class empleado
{
    public string nombre {get; set;}
    public int horasT {get; set;}
    public double sueldoH {get; set;}

    public empleado(string nombre, int horasT, double sueldoH){
        this.nombre = nombre;
        this.horasT = horasT;
        this.sueldoH = sueldoH;
    }
    
}